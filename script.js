let currentIndex = Math.floor(Math.random() * quotes.length);

function updateQuote() {
  const quoteElement = document.getElementById('quote');
  const progressElement = document.querySelector('.progress');
  
  quoteElement.classList.add('fade');
  
  // Reset progress bar animation
  progressElement.style.animation = 'none';
  progressElement.offsetHeight; // Trigger reflow
  progressElement.style.animation = 'progress 30s linear infinite';
  
  setTimeout(() => {
    let newIndex;
    do {
      newIndex = Math.floor(Math.random() * quotes.length);
    } while (newIndex === currentIndex);
    
    currentIndex = newIndex;
    quoteElement.textContent = quotes[currentIndex].text;
    
    quoteElement.classList.remove('fade');
  }, 300);
}

function copyQuote() {
  const quote = quotes[currentIndex].text;
  const fullQuote = `"${quote}"`;
  
  navigator.clipboard.writeText(fullQuote)
    .then(() => {
      const btn = document.querySelector('.copy-btn');
      btn.classList.add('copied');
      
      setTimeout(() => {
        btn.classList.remove('copied');
      }, 2000);
    })
    .catch(err => {
      console.error('Failed to copy text: ', err);
    });
}

// Initial quote display
updateQuote();

// Update quote every 30 seconds
setInterval(updateQuote, 30000);
